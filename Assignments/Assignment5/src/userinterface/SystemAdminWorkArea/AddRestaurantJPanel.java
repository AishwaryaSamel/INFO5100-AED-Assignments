    private void btnAdd2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdd2ActionPerformed
        if(ecosystem.getUserAccountDirectory().checkIfUsernameIsUnique(txtName.getText())){
            Restaurant restaurant = new Restaurant();
            restaurant.setName(txtName.getText());
            restaurant.setDecription(txtDesc.getText());
            restaurant.setManager(txtManager.getText());
            restaurant.setRating(Integer.parseInt(txtRating.getText()));
            ecosystem.getRestaurantDirectory().createRestaurant(restaurant);
            ecosystem.getEmployeeDirectory().createEmployee(txtManager.getText());
            ecosystem.getUserAccountDirectory().createUserAccount(txtManager.getText(), txtPassword.getText(), new AdminRole());
            JOptionPane.showMessageDialog(null, "Restaurant added successfully!!", "Info", JOptionPane.INFORMATION_MESSAGE);
        }
        else{
            JOptionPane.showMessageDialog(null, "Username already exists", "Info", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnAdd2ActionPerformed

