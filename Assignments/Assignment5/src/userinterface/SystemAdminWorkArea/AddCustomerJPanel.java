    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        if(ecosystem.getUserAccountDirectory().checkIfUsernameIsUnique(txtName.getText())){
            ecosystem.getCustomerDirectory().createCustomer(txtName.getText());
            //customerDirectory.createCustomer(txtName.getText());
            ecosystem.getUserAccountDirectory().createUserAccount(txtName.getText(), txtPassword.getText(), new CustomerRole());
            JOptionPane.showMessageDialog(null, "Customer added successfully!!", "Info", JOptionPane.INFORMATION_MESSAGE);
        }
        else{
            JOptionPane.showMessageDialog(null, "Username already exists", "Info", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnAddActionPerformed

