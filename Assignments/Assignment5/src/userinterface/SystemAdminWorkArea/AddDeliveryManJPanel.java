    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
    if(ecosystem.getUserAccountDirectory().checkIfUsernameIsUnique(txtName.getText())){
        ecosystem.getDeliveryManDirectory().createDeliveryMan(txtName.getText());
        ecosystem.getUserAccountDirectory().createUserAccount(txtName.getText(), txtPassword.getText(), new DeliverManRole());
        JOptionPane.showMessageDialog(null, "Delivery Man added successfully!!", "Info", JOptionPane.INFORMATION_MESSAGE);
    }
    else{
        JOptionPane.showMessageDialog(null, "Username already exists", "Info", JOptionPane.INFORMATION_MESSAGE);
    }
    }//GEN-LAST:event_btnAddActionPerformed

