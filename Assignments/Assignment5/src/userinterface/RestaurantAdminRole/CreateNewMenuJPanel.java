    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed

        Menu menu = new Menu();
        menu.setName(txtName.getText());
        menu.setPrice(Integer.parseInt(txtPrice.getText()));
        menu.setAvailability(Integer.parseInt(txtAvailability.getText()));
        restaurant.getMenuDirectory().createMenu(menu);
        

        JOptionPane.showMessageDialog(null, "Menu added!", "Info", JOptionPane.INFORMATION_MESSAGE);

    }//GEN-LAST:event_btnAddActionPerformed

    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed

       userProcessContainer.remove(this);
        Component[] componentArray = userProcessContainer.getComponents();
        Component component = componentArray[componentArray.length - 1];
        ManageMenuJPanel manageProductCatalogJPanel = (ManageMenuJPanel) component;
        manageProductCatalogJPanel.refreshTable();
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.previous(userProcessContainer);
    }//GEN-LAST:event_btnBackActionPerformed

