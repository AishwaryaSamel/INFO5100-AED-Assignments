    public void populateTable(){
        DefaultTableModel dtm = (DefaultTableModel) tblCarDetails.getModel();
        dtm.setRowCount(0);
 
        for (CarDetails carDetails : carDirectory.getCarDetails()){
            Object[] row = new Object[12];
            row[0] = carDetails;
            row[1] = carDetails.getMake();
            row[2] = carDetails.getAvailability();
            row[3] = carDetails.getYearOfManufacture();
            row[4] = carDetails.getNumberOfSeats();
            row[5] = carDetails.getAttributes();
            row[6] = carDetails.getModelNumber();
            row[7] = carDetails.getManufacturer();
            row[8] = carDetails.getLastUpdateDate();
            row[9] = carDetails.getCity();
            row[10] = carDetails.getCertExpiryDate();
            row[11] = carDetails.getSerialID();
            
            dtm.addRow(row);
        } 
          
        
        
        
    }

