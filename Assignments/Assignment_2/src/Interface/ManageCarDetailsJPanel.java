    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int selectedRow = tblCarDetails.getSelectedRow();
        if(selectedRow >0){
            int dialogButton = JOptionPane.YES_NO_OPTION;
            int dialogResult = JOptionPane.showConfirmDialog(null, "Would you like to delete the row?", "Warning", dialogButton);
            if(dialogResult == JOptionPane.YES_OPTION){
                CarDetails carDetails = (CarDetails) tblCarDetails.getValueAt(selectedRow, 0);
                carDirectory.deleteCarDetails(carDetails);
                populateTable();
            }
        }
        else{
            JOptionPane.showMessageDialog(null, "Please select row first","Warning",JOptionPane.WARNING_MESSAGE);
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_btnDeleteActionPerformed

