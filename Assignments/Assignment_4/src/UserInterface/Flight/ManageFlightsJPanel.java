        public void populate(){
        
        int rowCount = flightTable.getRowCount();
        DefaultTableModel model = (DefaultTableModel)flightTable.getModel();
        for(int i=rowCount-1;i>=0;i--) {
        model.removeRow(i);
        }
        
        DefaultTableModel dtm = (DefaultTableModel)flightTable.getModel();
        dtm.setRowCount(0);
        
        for(Flights a : airliner.getFlightsDirectory().getFlightsDirectory()){
            Object[] row = new Object[dtm.getColumnCount()];
            row[0]=a;
            row[1]=a.getPreferedTimeOfDay();
            row[2]=a.getDepartureLocation();
            row[3]=a.getArrivalLocation();
            row[4]=a.getDate();
            row[5]=a.getAirplane();
            row[6]=a.getPrice();
            dtm.addRow(row);
        }
    }

