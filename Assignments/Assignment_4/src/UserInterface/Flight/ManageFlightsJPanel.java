    private void viewFlightBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewFlightBtnActionPerformed
        // TODO add your handling code here:
        int SelectedRow = flightTable.getSelectedRow();
        if(SelectedRow >= 0)
        {
            Flights flight = (Flights) flightTable.getValueAt(SelectedRow, 0);
            ViewFlightJPanel viewpanel = new ViewFlightJPanel(rightPanel, flight);
            CardLayout cardLayout = (CardLayout)rightPanel.getLayout();
            rightPanel.add(viewpanel);
            cardLayout.next(rightPanel);
        }
        else
        {
            JOptionPane.showMessageDialog(null,"Please select a flight","Alert",JOptionPane.ERROR_MESSAGE);
        }  
    }//GEN-LAST:event_viewFlightBtnActionPerformed

