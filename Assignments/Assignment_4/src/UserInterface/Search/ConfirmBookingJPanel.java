    public void populateCustomerIdComboBox()
    {
        customerIdComboBox.removeAllItems();
        customerIdComboBox.addItem(" ");
        for(Customer c: airlinerDir.getCustomerDirectory().getCustomerDirectory()){
            customerIdComboBox.addItem(c);
        }
    }
    
    public void populateSeatComboBox(String Seat)
    {
        selectSeatComboBox.removeAllItems();
        selectSeatComboBox.addItem(" ");
        if(Seat == "Window")
        {
        selectSeatComboBox.removeAllItems();
        for(int i=0;i<25;i++)
        {
            for(int j=0; j<6; j++)
            {
                if(j==0 || j==5)
                    selectSeatComboBox.addItem(this.availableSeats[i][j]);
            }
        }        
        }
        if(Seat == "Aisle")
        {
         for(int i=0;i<25;i++)
        {
            for(int j=0; j<6; j++)
            {
                if(j==2 || j==3)
                    selectSeatComboBox.addItem(this.availableSeats[i][j]);
            }
        } 
        }
        if(Seat == "Middle")
        {
         for(int i=0;i<25;i++)
        {
            for(int j=0; j<6; j++)
            {
                if(j==1 || j==4)
                    selectSeatComboBox.addItem(this.availableSeats[i][j]);
            }
        } 
        }
    }
    
    private void modifySeatComboBox()
    {
            String selectedSeat = String.valueOf(selectSeatComboBox.getSelectedItem());
                String[][] alteredArray = flight.getSeatDirectory().reserveSeat(selectedSeat);

                selectSeatComboBox.removeAllItems();
                for(int i=0;i<25;i++)
                {
                    for(int j=0; j<6; j++)
                    {
                            selectSeatComboBox.addItem(alteredArray[i][j]);
                    }
                }
    }
    
    private void modifyCustomerTable()
    {
        Customer c = (Customer) customerIdComboBox.getSelectedItem();
        c.setAirliner(airliner.toString());
        c.setSeat(selectSeatComboBox.getSelectedItem().toString());
        c.setFlightID(flight.toString());
    }
    
