    private void confirmBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmBtnActionPerformed
        // TODO add your handling code here:
        if(customerIdComboBox.getSelectedItem() == " ")
            {
                JOptionPane.showMessageDialog(this, "Please select a customer.");
            }
        if(customerIdComboBox.getSelectedItem() == " ")
            {
                JOptionPane.showMessageDialog(this, "Please select a seat.");
            }
        else
        {
            String selectedSeat = String.valueOf(selectSeatComboBox.getSelectedItem());
            if(selectedSeat.contains("Not Available"))
            {
                  JOptionPane.showMessageDialog(this, "Seat not available.");
                  return;
            }
            else{
                 int input = JOptionPane.showConfirmDialog(null, 
                "Do you want to confirm the booking?", "Select an Option...",JOptionPane.YES_NO_CANCEL_OPTION);
                if(input == 0)
                {
                JOptionPane.showMessageDialog(this, "Booking Confirmed");
                }
            modifyCustomerTable();
            modifySeatComboBox();
            }         
        }
    }//GEN-LAST:event_confirmBtnActionPerformed

